package KI304.Pliuvak.Lab6;

/**
 * Спільний “контракт” для всього, що можна зберігати у відсіку для зброї.
 * Елемент має розмір (умовний об'єм/маса) і вміє друкувати себе.
 */
public interface ArmoryItem extends Comparable<ArmoryItem> {
    /**
     * Повертає “розмір” елемента (чим менше — тим менший елемент).
     * Це поле використовується для порівняння та пошуку мінімального.
     */
    int getSize();

    /** Друк інформації про елемент (зручно для демонстрації в консолі). */
    void print();
}
